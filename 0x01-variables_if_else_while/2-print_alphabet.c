#include <stdio.h>
/**
* main - print if the number is postive, zero, or negative
* Description: using the main function
* program that prints the alphabet in lowercase, followed by a new line
* Return: 0
*/
int main(void)
{
char al;
for (al = 'a' ; al <= 'z' ; al++)
{
putchar(al);
}
putchar('\n');
return (0);
}
